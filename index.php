<h2>
   Features
</h2>
<p>
   The new version of the CMS is an improvement over the previous version 2.6. Some of the new features of Pragyan v3.0 :</p>
<ul>
   <li>
       Multi-user open source content management system</li>
   <li>
       Organize and facilitate collaborative content creation and updation</li>
   <li>
       Integrated CkEditor v3.1 for easy editing of pages from the web</li>
   <li>
       Choose from a number of pre-installed widgets and configure them as per your needs</li>
   <li>
       Handy tools to manage permissions for groups and users</li>
   <li>
       Quickly create pages of type article, gallery, quiz, form, forum, poll, etc.</li>
   <li>
       Support for OpenID</li>
   <li>
       Template installation module with compatibility check</li>
   <li>
       Have page-specific templates so every page can have its own template</li>
   <li>
       Easily convert pages into PDF</li>
   <li>
       Integrated Google Maps and Latex plugins in articles</li>
   <li>
       Powerful search engine powered by Sphider</li>
   <li>
       Send mass emails to site registrants</li>
   <li>
       Security and protection against hacking attempts ( like XSS and Injections )</li>
   <li>
       Use captcha (without internet) or ReCaptcha (with internet) for avoiding spams</li>
   <li>
       Associate icons with every page using icons management</li>
   <li>
       Save articles as drafts and edit them later</li>
   <li>
       Censor specific words in articles and forum posts</li>
   <li>
       Easily install new modules using the module management</li>
   <li>
       Edit the registration form and user profile information fields as per your needs</li>
</ul>

